package org.icube.owen.employee;

public class LanguageDetails {

	private int employeeId;
	private int languageDetailsId;
	private int languageId;
	private String languageName;

	public int getEmployeeId() {
		return employeeId;
	}

	public void setEmployeeId(int employeeId) {
		this.employeeId = employeeId;
	}

	public int getLanguageDetailsId() {
		return languageDetailsId;
	}

	public void setLanguageDetailsId(int languageDetailsId) {
		this.languageDetailsId = languageDetailsId;
	}

	public int getLanguageId() {
		return languageId;
	}

	public void setLanguageId(int languageId) {
		this.languageId = languageId;
	}

	public String getLanguageName() {
		return languageName;
	}

	public void setLanguageName(String languageName) {
		this.languageName = languageName;
	}
}
