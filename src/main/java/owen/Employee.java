package owen;

public class Employee {

	private String id;
	private String name;
	private long score;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getScore() {
		return score;
	}

	public void setScore(Long long1) {
		this.score = long1;
	}

}
